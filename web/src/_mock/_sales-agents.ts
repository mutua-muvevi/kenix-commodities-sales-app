// Helper function to get overall stats
export function getSalesAgentStats(): SalesAgentStats {
  const totalAgents = _salesAgents.length;
  const approvedAgents = _salesAgents.filter((a) => a.approvalStatus === 'approved').length;
  const pendingAgents = _salesAgents.filter((a) => a.approvalStatus === 'pending').length;
  const bannedAgents = _salesAgents.filter((a) => a.approvalStatus === 'banned').length;

  const totalShopsRegistered = _salesAgents.reduce((sum, agent) => sum + agent.shopsRegistered, 0);
  const totalOrdersPlaced = _salesAgents.reduce((sum, agent) => sum + agent.ordersPlaced, 0);
  const totalRevenue = _salesAgents.reduce((sum, agent) => sum + agent.totalRevenue, 0);
  const totalCommissionPaid = _salesAgents.reduce((sum, agent) => sum + agent.totalCommission, 0);

  const weeklyRevenue = _salesAgents.reduce((sum, agent) => sum + agent.weeklyMetrics.revenue, 0);
  const weeklyCommission = _salesAgents.reduce(
    (sum, agent) => sum + agent.weeklyMetrics.commission,
    0
  );
  const monthlyRevenue = _salesAgents.reduce((sum, agent) => sum + agent.monthlyMetrics.revenue, 0);
  const monthlyCommission = _salesAgents.reduce(
    (sum, agent) => sum + agent.monthlyMetrics.commission,
    0
  );

  return {
    totalAgents,
    approvedAgents,
    pendingAgents,
    bannedAgents,
    totalShopsRegistered,
    totalOrdersPlaced,
    totalRevenue,
    totalCommissionPaid,
    weeklyRevenue,
    weeklyCommission,
    monthlyRevenue,
    monthlyCommission,
  };
}
